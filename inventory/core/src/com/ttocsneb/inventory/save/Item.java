package com.ttocsneb.inventory.save;

/**
 * An Item is an object that contains information for Stock that you can
 * sell.<br>
 * 
 * Including:
 * <ul>
 * <li>Name</li>
 * <li>Weight</li>
 * <li>price</li>
 * <li>stock</li>
 * </ul>
 */
public class Item {

	public String name;
	public float weight;
	public float price;
	public int stock;
	public short id;

}
